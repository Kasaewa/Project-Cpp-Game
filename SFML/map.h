void randomMapGenerate(String * TileMap) {

	int randomElementX = 0;
	int randomElementY = 0;
	srand(time(0));	
	int countMonet = 10;
	int countHealth = 2;

	while (countMonet>0) {
		randomElementX = 1 + rand() % (Width - 1);
		randomElementY = 1 + rand() % (Heigh - 1);

		if (TileMap[randomElementY][randomElementX] == ' ') {
			TileMap[randomElementY][randomElementX] = 's';
			countMonet--;
		}
	}
	while (countHealth>0) {
		randomElementX = 1 + rand() % (Width - 1);
			randomElementY = 1 + rand() % (Heigh - 1);

		if (TileMap[randomElementY][randomElementX] == ' ') {
			TileMap[randomElementY][randomElementX] = 'h';
			countHealth--;
		}
	}
}

